package ccc.java.kmeans;

public class ClusteringException extends Exception
{
	  public ClusteringException() { super(); }
	  public ClusteringException(String message) { super(message); }
	  public ClusteringException(String message, Throwable cause) { super(message, cause); }
	  public ClusteringException(Throwable cause) { super(cause); }

}
